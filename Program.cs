        if (mem == null)
        {
            return true;
        }
        return false;
    }

    public static byte[] Decrypt(byte[] enc, byte key)
    {
        byte[] output = new byte[enc.Length];
        for (int i = 0; i < enc.Length; i++)
        {
            output[i] = (byte)(enc[i] ^ key);
        }
        return  output;
